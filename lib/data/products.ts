export const getProductsByCategory = (categoryId: string): Product[] => {
  return products.filter(p => p.category === categoryId)
}

export const getProductById = (id: string): Product | undefined => {
  return products.find(p => p.id === id)
}

export const getBestsellers = (): Product[] => {
  return products.filter(p => p.badge === 'bestseller').slice(0, 8)
}

export const getNewArrivals = (): Product[] => {
  return products.filter(p => p.badge === 'new').slice(0, 8)
}

export const getHotProducts = (): Product[] => {
  return products.filter(p => p.badge === 'hot').slice(0, 8)
}

export const getSaleProducts = (): Product[] => {
  return products.filter(p => p.badge === 'sale')
}
